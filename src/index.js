const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    let a;
    let x;
    let r;
    let str = '';
    a = expr.match(/.{1,10}/g);
    for(let i=0; i<a.length; i++){
    if(a[i]==='**********'){
     r=' ';
    }
    else{
        x = a[i].replace(/\b0+/g, "");
        x1 = x.replace(/10/g, '.' );
        x2 = x1.replace(/11/g, '-' );
        r = MORSE_TABLE[x2];
    }
    str += String(r);   
}

return str;
}

